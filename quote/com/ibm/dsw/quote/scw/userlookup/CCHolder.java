package com.ibm.dsw.quote.scw.userlookup;

public class CCHolder {
	
	private String firstName;
	private String lastName;
	private String ccHolderPhoneNumber;
	private String emailAddr;
	private String ccHolderFaxNumber;
	
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	
	public String getCcHolderPhoneNumber() {
		return ccHolderPhoneNumber;
	}
	public void setCcHolderPhoneNumber(String ccHolderPhoneNumber) {
		this.ccHolderPhoneNumber = ccHolderPhoneNumber;
	}
	
	public String getEmailAddr() {
		return emailAddr;
	}
	public void setEmailAddr(String emailAddr) {
		this.emailAddr = emailAddr;
	}
	
	public String getCcHolderFaxNumber() {
		return ccHolderFaxNumber;
	}
	public void setCcHolderFaxNumber(String ccHolderFaxNumber) {
		this.ccHolderFaxNumber = ccHolderFaxNumber;
	}
}
